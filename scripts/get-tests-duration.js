/**
 * Allure Test Duration Script
 *
 * Processes test execution timeline data from Allure reporter and generates
 * a summary of test execution times aggregated by test ID. The script reads
 * timeline data, extracts test durations and IDs, aggregates execution times
 * by test ID, persists results to a file, and outputs formatted statistics
 * to the console.
 *
 * Usage:
 * 1. Run your Cypress tests with Allure reporter enabled to generate the timeline.json file.
 * 2. Execute this script to process the timeline data and generate testsExecutionTime.json.
 * 3. Use the generated testsExecutionTime.json file in your parallel-run.js script to balance test execution across threads.
 *
 * Output format:
 * - Console output: Formatted list of individual tests and aggregated execution times
 * - File output: JSON file with structure { "C1234": 120000, "C5678": 300000, ... }
 */
const fs = require('fs');

const ALLURE_TIMELINE_FILE = './timeline.json'; // file generated by Allure reporter with tests execution times
const TESTS_EXECUTION_TIME_FILE = './doc/testsExecutionTime.json'; // file with tests execution times, generated by this script. Has format { "C1234": 120000, "C5678": 300000, ... }

const timelineContent = fs.readFileSync(ALLURE_TIMELINE_FILE, { encoding: 'utf8' });
const { children: timeLine } = JSON.parse(timelineContent);

let oldData = {};
if (fs.existsSync(TESTS_EXECUTION_TIME_FILE)) {
  try {
    oldData = JSON.parse(fs.readFileSync(TESTS_EXECUTION_TIME_FILE, { encoding: 'utf8' }));
  } catch (error) {
    console.error('Error reading old data file:', error);
  }
}

// Extract test durations and IDs, and sort by duration in descending order
const executionTimeList = timeLine
  .filter(({ status }) => status === 'passed')
  .map((test) => {
    return {
      name: test.name,
      duration: test.time.duration,
      testId: test.name.match(/\bC\d+\b/)[0] || null,
    };
  })
  .sort((a, b) => b.duration - a.duration);

// Aggregate execution times by test ID
const testsExecutionTime = {};
executionTimeList.forEach((test) => {
  if (test.testId && Object.keys(testsExecutionTime).includes(test.testId)) {
    testsExecutionTime[test.testId] += test.duration;
  } else if (test.testId) {
    testsExecutionTime[test.testId] = test.duration;
  }
});

// Merge with old data, keeping the maximum duration for each test ID
for (const [testId, duration] of Object.entries(testsExecutionTime)) {
  if (oldData[testId]) {
    oldData[testId] = Math.round((oldData[testId] + duration) / 2);
  } else {
    oldData[testId] = duration;
  }
}

oldData = Object.fromEntries(Object.entries(oldData).sort(([, a], [, b]) => b - a)); // sort by duration in descending order

fs.writeFileSync(TESTS_EXECUTION_TIME_FILE, JSON.stringify(oldData, null, 2), {
  encoding: 'utf8',
});

// ---------------- Printing results ----------------
Object.entries(oldData).forEach(([testId, duration]) => {
  console.log(`${duration}ms`.padEnd(10) + `${testId}`.padEnd(10));
});
console.log(`Total unique test IDs count: ${Object.keys(oldData).length}`);
